                        <h3>Коды ошибок стиральных машин</h3>

                        <p>
                            Все современные автоматические стиральные машинки оснащены системой самодиагностики, и способны выявлять неисправности уже в процессе их зарождения. Конечно, эти системы различаются, в зависимости от класса прибора и марки, однако принципы работы весьма схож.
                            <br>
                            <br> Владелец прибора узнает о работе системы самодиагностики через дисплей или индикатор, куда выдается код ошибки. Это происходит в случае, если обнаружена неисправность или некорректная работа основных систем машинки. Значение кода на дисплей, как правило, не выводится, поэтому чтобы узнать, что приключилось с драгоценным аппаратом, придется найти его руководство по эксплуатации или прокрутить эту страницу ниже и отыскать значение кода в таблице.
                        </p>
