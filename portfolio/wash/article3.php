                        <h3>При какой температуре стирать</h3>

                        <p>

                            <span class="negr">Холодная вода (до 30 градусов)</span>
                            <br> Стирка в холодной воде – единственный способ обеспечить безопасный уход для деликатных тканей в домашних условиях. Холодная вода обеспечивает наименьшую деформацию волокон и по минимуму влияет на стойкость окраски. В холодной воде стирают дорогие натуральные ткани, требующие особенной бережности при стирке:
                            <br> Шерсть;
                            <br> Натуральный шелк;
                            <br> Кружево и т.д.
                            <br>
                            <br>
                            <br>
                            <span class="negr">Теплая вода (30-40 градусов)</span>
                            <br> Прекрасно подходит для регулярной и ежедневной стирки одежды и домашнего текстиля. В таком режиме эффективно отстирываются легкие и средние загрязнения, удаление же пятен требует предварительного замачивания или замывания. Стирать в теплой воде разрешается практически все виды тканей.
                            <br>
                            <br>
                            <span class="negr">Горячая вода (40-60 градусов)</span>
                            <br> Данный температурный диапазон относится к интенсивной стирке, которой под силу устранить даже стойкие и существенные загрязнения. Стойкие пятна рекомендуется обработать каким-либо средством для предварительной стирки, замачивать при этом необязательно. Интенсивная стирка показана постельному белью, детским вещам, на которых всегда имеется множество пятен, полотенцам, рабочей одежде и другим видам изделий из ткани, которые в процессе использования сильно загрязняются.
                            <br>
                            <br>
                            <span class="negr">Кипяток (95 градусов)</span>
                            <br> Кипячение – разновидность интенсивной стирки, которая не только очищает, но и дезинфицирует. Необходимость в проведении кипячения существует для строго определенных групп текстильных изделий:
                            <br>
                            <br> одежда для новорожденных;
                            <br> текстильные игрушки;
                            <br> одежда для людей, страдающих заболеваниями кожи;
                            <br> товары медицинского назначения: перевязочные материалы, нательное белье и т.д.
                            <br>
                            <br> В ряде случаев возможна стирка в кипятке обычной одежды и предметов быта, например, если есть необходимость эффективно их отбелить или устранить сверхстойкие пятна, которые не поддаются другим видам стирки. Кипятить можно начинать после того, как Вы убедились в допустимости данной манипуляции для конкретной вещи. В любом случае, кипячение противопоказано деликатным тканям, шерстяным вещам и трикотажным изделиям.
